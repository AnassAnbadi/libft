#include "libft.h"

int	ft_atoi(const char *str)
{
	int		sign;
	int		i;
	long	n;

	i = 0;
	sign = 1;
	n = 0;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t' || str[i] == '\v'
		|| str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		if (n > (9223372036854775807 - (str[i] - '0')) / 10)
			return ((-sign - 1) / 2);
		n = n * 10 + (str[i] - '0');
		i++;
	}
	return ((int)(n * sign));
}
